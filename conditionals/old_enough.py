age = int(input("how old are you?"))

if age >= 18:  
    print("You can vote.")  
elif age >= 16:  
    print("You can drive.")  
elif age >= 15:  
    print("You can get a learner's permit.")  
elif age >= 5:  
    print("You can go to school.")  
else:  
    print("You're not old enough for any of these.")  
